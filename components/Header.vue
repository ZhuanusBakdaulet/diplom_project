import login from "@/mixins/login";
export default {
  mixins: [login],
  data: () => ({
    user_type_div: false,
    search_div: false,
    hover_city: false,
    dropdown: false,
    searching: "",
    catalog: false,
    hover: 0,
    subhover: 0,
    categories: []
  }),
  watch: {
    "$auth.loggedIn"() {
      if (this.$auth.loggedIn) this.sessionAttach();
      this.$store.dispatch("fetchBasket");
    },
    "$route.path"(newVal, oldVal) {
      if ("/Products" != newVal) this.searching = "";
    }
  },
  mounted() {
    this.$store.dispatch("fetchBasket");
    this.fetchCategories();
  },
  computed: {
    toBasket() {
      if (this.$store.state.basketPrice == 0) return "/";
      return "/Basket";
    },
    isAdmin() {
      if (this.$auth.user.is_superuser) return true;
      else if (this.$auth.user.is_staff) return true;
      else false;
    },
    isProvider() {
      if (
        this.$auth.user.user_type == "P" ||
        this.$auth.user.user_type == "C" ||
        this.$auth.user.user_type == "S"
      )
        return true;
      return false;
    }
  },
  methods: {
    showUserTypeChange() {
      if (!this.$auth.loggedIn) this.user_type_div = !this.user_type_div;
    },
    getPosition(item, index) {
      this.hover = item;
      const category = document.querySelectorAll(".item-parent")[index];
      category.addEventListener("mousemove", this.setPosToSubCategory);
    },
    setPosToSubCategory(event) {
      const category = document.querySelector(".categories");
      const sub_category = document.querySelector(".sub-categories");
      let mouse_pos =
        event.pageY - category.getBoundingClientRect().top - window.scrollY;
      let pos = mouse_pos - Math.ceil(mouse_pos % 50);
      sub_category.style.top = `${pos}px`;
    },
    searchIt() {
      this.$router.push({
        path: "/Products",
        query: { search: this.searching }
      });
      this.search_div = false;
    },
    changeCity(item) {
      this.$store.commit("setMyCity", item);
      this.hover_city = false;
    },
    async sessionAttach() {
      let body = {
        session_key: localStorage.session_key
      };
      if (
        this.$auth.user.user_type == "E" ||
        this.$auth.user.user_type == "I"
      ) {
        await this.$axios.$post(`orders/attach/`, body).catch(err => {
          console.log(err);
          // let error = Object.values(err.response.data).join(", ");
          // this.$notifier.showMessage({ color: "error", text: error });
        });
      }
    },
    dropDown() {
      if (this.$auth.loggedIn) {
        if (
          this.$auth.user.user_type != "E" &&
          this.$auth.user.user_type != "I"
        ) {
          this.dropdown = true;
        }
      } else this.dropdown = false;
    },
    changeClientType(value) {
      if (!this.$auth.loggedIn) this.$store.commit("setClientType", value);
      this.user_type_div = false;
    },
    goToProducts(item, type) {
      let query = {};
      if (type == "category") query.category = [item.id];
      else {
        query.category = [item.category];
        query.sub_category = [item.id];
      }
      this.$router.push({
        path: "/Products",
        query
      });
    },
    async fetchCategories() {
      await this.$axios.$get(`categories/`).then(res => {
        this.categories = res.results;
      });
    }
  }
};
